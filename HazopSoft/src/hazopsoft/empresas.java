    private DefaultTableModel getModelo(){
        DefaultTableModel model = new DefaultTableModel(
        null, new String [] {"empresas","Nombre de Empresa"}){
        Class[] types = new Class []{
                Empresas.class,
                java.lang.String.class
        };
        boolean[] canEdit = new boolean [] {
        false,false
        };
        @Override
        public Class getColumnClass(int columnIndex) {
           return types [columnIndex];
        }
        @Override
        public boolean isCellEditable(int rowIndex, int colIndex){
           return canEdit [colIndex];
        }
    };  
        return model;
    }
    
    
    private void ModeloEmpleado(){
        modelo = getModelo();
        jTable1.setModel(modelo);
        jTable1.getTableHeader().setReorderingAllowed(false);
        jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        funciones.setOcultarColumnas(jTable1, new int[]{0});
//        funciones.setSizeColumnas(jTable1, new int[]{1}, new int[]{80});
    }
    
    private void loadData(){
        if(empresasJpaC==null)empresasJpaC = new EmpresasJpaController(factory);
        List<Empresas> empresas = empresasJpaC.findEmpresasEntities();
        for(Empresas ae:empresas){
            int rows= modelo.getRowCount();
            modelo.addRow(dato);
            modelo.setValueAt(ae, rows, 0);
            modelo.setValueAt(ae.getNombreEmpresa(), rows, 1);
        }
    }


